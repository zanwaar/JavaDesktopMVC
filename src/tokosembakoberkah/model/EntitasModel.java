// Entitas.java (Model)
public class EntitasModel {

    private int id;
    private String type;
    private String nama;
    private String alamat;
    private String noTelp;

    public EntitasModel(int id, String type, String nama, String noTelp, String alamat) {
        this.id = id;
        this.type = type;
        this.nama = nama;
        this.noTelp = noTelp;
        this.alamat = alamat;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNoTelp() {
        return noTelp;
    }

    public void setNoTelp(String noTelp) {
        this.noTelp = noTelp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
